package Basics;

public class EscapeSequences {
    // I study in Ghousia \College\
    public static void main(String[] args) {
        // I study in Ghousia \College\
        // I study in "Ghousia" College
        System.out.println("I study in Ghousia \\College\\");
        System.out.println("I study in \"Ghousia\" College ");
        System.out.println(" We are studying \n JAVA");
        System.out.println(" We are studying \t Java");
    }
}
